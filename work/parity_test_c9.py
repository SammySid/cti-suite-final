def feP(T, P):
    """Bilinear interpolation from 2D probed table (mirrors JS fEnhanceAtP v2.6.0)."""
    # P → alt_m
    alt_m = (1.0 - (P/101.325)**(1.0/5.2559)) / 2.25577e-5
    # T index
    t_float = (T - F_ALT_T_MIN) / F_ALT_T_STEP
    t_lo = max(0, min(F_ALT_T_N - 2, int(math.floor(t_float))))
    frac_t = max(0.0, min(1.0, t_float - t_lo))
    # alt index
    a_float = alt_m / F_ALT_A_STEP
    a_lo = max(0, min(len(F_ALT_TABLE) - 2, int(math.floor(a_float))))
    frac_a = max(0.0, min(1.0, a_float - a_lo))
    # bilinear interp
    rLo = F_ALT_TABLE[a_lo]; rHi = F_ALT_TABLE[a_lo + 1]
    fLo = rLo[t_lo] + frac_t * (rLo[t_lo+1] - rLo[t_lo])
    fHi = rHi[t_lo] + frac_t * (rHi[t_lo+1] - rHi[t_lo])
    return fLo + frac_a * (fHi - fLo)

def dp_explicit(Pw):
    if Pw <= 0: return 0.0
    a = math.log(Pw)
    dp = DP_C14 + DP_C15*a + DP_C16*a*a + DP_C17*a**3 + DP_C18*Pw**DP_n
    if dp < 0:
        dp = ICE_C14 + ICE_C15*a + ICE_C16*a*a
    return dp

def dp_newton(W, P, f_init):
    if W <= 0: return 0.0
    denom = (MW_MA + W) * f_init
    Pw = P*W/denom if denom > 0 else 0
    dp = dp_explicit(Pw)
    for _ in range(2):
        fd = feP(dp, P); denom = (MW_MA + W)*fd
        Pw = P*W/denom if denom > 0 else 0; dp = dp_explicit(Pw)
    step = 1.0
    for _ in range(50):
        Pws_dp = pws(dp); fd = feP(dp, P)
        fPws = fd*Pws_dp; dn = P - fPws
        if abs(dn) < 1e-15: break
        W_dp = MW_MA*fPws/dn
        if W_dp <= 0: break
        if abs(W/W_dp - 1) < 1e-6: break
        if abs(step) < 0.0001: break
        dPws = dpwsDT(dp, Pws_dp)
        dW_dp = MW_MA*fd*dPws*P/(dn*dn)
        if abs(dW_dp) < 1e-20: break
        step = (W - W_dp)/dW_dp; dp += step
    return dp

